	public static void main(String[] args) {
//		Scanner sc = new Scanner(System.in);
		Scanner sc = new Scanner(input);

		int T = 10;
		for (int t = 1; t <= T; t++) {
			Stack<Character> st = new Stack<>();
			List<Character> list = new ArrayList<>(Arrays.asList('(', '[', '{', '<'));
			Map<Character, Character> map = new HashMap<>();
			map.put(')', '(');
			map.put(']', '[');
			map.put('}', '{');
			map.put('>', '<');

			int ans = 1;
			int len = sc.nextInt();
			String line = sc.next();
			for (int i = 0; i < len; i++) {
				char c = line.charAt(i);
				if (list.contains(c)) {
					st.push(c);
				} else if (!st.isEmpty() && map.get(c) == st.peek()) {
					st.pop();
				} else {
					ans = 0;
					break;
				}
			}

			System.out.println(String.format("#%d %d", t, ans));

		}

		sc.close();
	}

